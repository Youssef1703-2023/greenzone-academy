export function getGlossaryCategoryLabel(categoryId, language) {
  const category = glossaryCategories.find((item) => item.id === categoryId);
  if (!category) return categoryId;
  return language === 'ar' ? category.labelAr : category.labelEn;
}

export function getGlossaryAliasEntries() {
  return glossaryEntries
    .flatMap((entry) => entry.aliases.map((alias) => ({ alias, entry })))
    .sort((a, b) => b.alias.length - a.alias.length);
}
